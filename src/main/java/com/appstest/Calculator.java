package com.appstest;

public class Calculator {

    public int integerAdd(int a, int b){
        return a+b;
    }

    public int integerSubtraction(int a, int b){
        return a-b;
    }

    public int integerMultiply(int a, int b){
        return a*b;
    }

    public int integerDivide(int a, int b){
        return a/b;
    }
}


